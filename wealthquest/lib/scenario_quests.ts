export function getScenarioQuest(questId: number): ScenarioData | null {
  return SCENARIO_QUESTS.find(s => s.questId === questId) || null
}

export function getBossQuests(): ScenarioData[] {
  return SCENARIO_QUESTS.filter(q => q.isBoss)
}

export function getQuestsBySkill(skill: string): ScenarioData[] {
  return SCENARIO_QUESTS.filter(q => q.skill === skill)
}

export function getQuestsByDifficulty(difficulty: ScenarioData['difficulty']): ScenarioData[] {
  return SCENARIO_QUESTS.filter(q => q.difficulty === difficulty)
}
